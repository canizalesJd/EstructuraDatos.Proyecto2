    // Obtener informacion del departamento seleccionado
    private void tablaDepartamentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaDepartamentoMouseClicked
        // Referencia: https://stackoverflow.com/questions/29345792/java-jtable-getting-the-data-of-the-selected-row
        int fila = tablaDepartamento.getSelectedRow();
        if (fila >= 0) {
            // Obtener ID del departamento seleccionado
            int id = (int) tablaDepartamento.getValueAt(fila, 0);
            if (id > 0) {
                // Mostrar datos en los inputs
                this.idDepartamento.setText(String.valueOf(id));
                habilitarCampos();
                cargarArticulosEnTabla();
            }
        }
    }//GEN-LAST:event_tablaDepartamentoMouseClicked

