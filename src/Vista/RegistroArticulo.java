    // Limpia todos los campos del formulario dejandolos en su estado inicial.
    private void limpiarFormulario() {
        this.nombre.setText("");
        this.idArticulo.setText(
            String.valueOf(controlador.getContadorArticulosGlobal() + 1)
        );
        this.categoria.setSelectedIndex(0);
    }
    
    /**
     * Muestra el listado de departamentos de forma tabular.
     * Si no hay registros, muestra un mensaje de “No hay registros”.
     *
    */
    private void mostrarRegistrosEnTablaArticulo() {
        // Referencia: https://stackoverflow.com/questions/2732865/how-to-update-jtable
        DefaultTableModel modelo = (DefaultTableModel) tablaArticulo.getModel();
        modelo.setRowCount(0); // Limpiar tabla
                     
        // Obtener articulos del departamento seleccionado
        int idDepartamento = Integer.parseInt(
            this.idDepartamento.getText().trim()
        );
        
        Departamento departamento = controlador.buscarDepartamento(idDepartamento);
        Articulo[] articulos = departamento.getArticulos();
        int contador = departamento.getContadorArticulos();
        // Si no hay articulos, mostrar el mensaje en la tabla
        if (contador == 0) {
            modelo.addRow(new Object[] { "", "No hay articulos registrados", "" });
            return;
        }
        
        // Recorrer array de articulos
        for (int i = 0; i < contador; i++) {
            Articulo articulo = articulos[i];
            // Validar que no sea null
            if (articulo == null) {
                continue;
            }
            Object[] fila = new Object[] {
                articulo.getId(),
                articulo.getNombre(),
                articulo.getCategoria()
            };
            modelo.addRow(fila);
        }

    }
      
    /**
     * Carga y muestra todos los articulos de un departamento.
    */
    private void cargarArticulosEnTabla() {
        try {
            mostrarRegistrosEnTablaArticulo();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Error al listar registros: " + e.getMessage(),
                "Error de Listado",
                JOptionPane.ERROR_MESSAGE);
        }
    }
    
    /**
     * Carga y muestra todos los registros.
    */
    private void cargarDepartamentosEnTabla() {
        try {
            mostrarRegistrosEnTablaDepartamento();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Error al listar registros: " + e.getMessage(),
                "Error de Listado",
                JOptionPane.ERROR_MESSAGE);
        }
    }
    
    /**
     * Muestra el listado de departamentos de forma tabular.
     * Si no hay registros, muestra un mensaje de “No hay registros”.
     *
    */
    private void mostrarRegistrosEnTablaDepartamento() {
        // Referencia: https://stackoverflow.com/questions/2732865/how-to-update-jtable
        DefaultTableModel modelo = (DefaultTableModel) tablaDepartamento.getModel();
        modelo.setRowCount(0); // Limpiar tabla
              
        boolean hayRegistros = false;
        
        // Obtener departamentos actuales
        Departamento[] departamentos = controlador.obtenerDepartamentosActuales();
       
        // Recorrer array de departamentos
        for (Departamento departamento : departamentos) {
            hayRegistros = true;

            // Llenar filas
            Object[] fila = new Object[] {
                departamento.getId(),
                departamento.getNombre(),
                departamento.getContadorArticulos()
            };
            modelo.addRow(fila);
        }
        // Desplegar fila en caso de no tener registros
        if (!hayRegistros) {
            modelo.addRow(new Object[] { "", "No hay departamentos registrados", "" });
        }
    }
    
