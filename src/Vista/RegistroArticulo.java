    // Valida que los campos del formulario no esten vacios antes de registrar.
    private void validarCampos() {
        String nombre = this.nombre.getText().trim();
        if (nombre.isEmpty()) {
            throw new IllegalArgumentException("El campo nombre es obligatorio");
        }
        String categoria = (String) this.categoria.getSelectedItem();
        if (categoria == null  ) {
            throw new IllegalArgumentException("Debe seleccionar una categoria");
        }
    }

    /**
    * Habilita los campos y botones del formulario
    * Se llama al seleccionar un registro de la tabla.
    */
    private void habilitarCampos() {
        this.nombre.setEnabled(true);
        this.categoria.setEnabled(true);
        this.botonRegistrar.setEnabled(true);
        this.botonCancelar.setEnabled(true);
    }
    
    /**
     * Captura los datos del formulario, valida su formato e intenta
     * registrar el articulo.
    */
    public void capturarDatosFormulario() {
        try {
            validarCampos();
            String nombre = this.nombre.getText().trim();
            int idDepartamento = Integer.parseInt(
                this.idDepartamento.getText().trim()
            );
            String categoria = (String) this.categoria.getSelectedItem();
            controlador.agregarArticulo(
                idDepartamento,
                nombre,
                categoria
            );
            limpiarFormulario();
            cargarArticulosEnTabla();
        } catch (IllegalArgumentException e) {
            // Error de validación - mensaje específico
            JOptionPane.showMessageDialog(this, 
                e.getMessage(), 
                "Error de Validacion", 
                JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
             // Error inesperado
             JOptionPane.showMessageDialog(this, 
                 "Error inesperado: " + e.getMessage(), 
                 "Error", 
                 JOptionPane.ERROR_MESSAGE);
         }
    }
    
    
       
